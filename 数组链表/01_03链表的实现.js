// 单链表节点

class LinkedListNode {
  constructor(value, next) {
    this.value = value;
    this.next = next;
  }
}
// 双链表节点

class DoublyLinkedListNode {
  constructor(value, previous, next) {
    this.value = value;
    this.previous = previous;
    this.next = next;
  }
}

